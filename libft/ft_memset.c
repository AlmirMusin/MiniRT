#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*new_str;

	i = 0;
	new_str = b;
	while (i < len)
	{
		new_str[i] = c;
		i++;
	}
	return (b);
}
